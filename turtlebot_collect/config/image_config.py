#!/usr/bin/env python
# coding=utf-8
'''
图像处理配置

本文件定义了图像预处理的参数，包括输出尺寸和裁剪比例。
这些参数需要与训练模型时使用的参数保持一致。

使用方法:
    from config import ImageConfig
    
    width = ImageConfig.OUTPUT_WIDTH    # 200
    height = ImageConfig.OUTPUT_HEIGHT  # 88
    crop_top = ImageConfig.CROP_TOP_RATIO  # 0.3

图像处理流程:
    原始图像 (如 640x480)
        ↓ 裁剪顶部 30%（去除天空）
    裁剪后图像 (如 640x336)
        ↓ 缩放到目标尺寸
    输出图像 (200x88)
'''


class ImageConfig:
    """
    图像处理参数配置
    
    这些参数决定了输入神经网络的图像格式，
    必须与 CARLA 训练数据的格式保持一致。
    """
    
    # ============ 输出尺寸 ============
    # 神经网络输入图像的尺寸
    # 与 CARLA 端到端驾驶模型的输入尺寸保持一致
    
    # 输出图像宽度 (像素)
    # 200 是 CARLA 训练数据的标准宽度
    OUTPUT_WIDTH = 200
    
    # 输出图像高度 (像素)
    # 88 是 CARLA 训练数据的标准高度
    OUTPUT_HEIGHT = 88
    
    # ============ 裁剪参数 ============
    # 裁剪用于去除图像中与驾驶无关的区域
    # 比例值范围: 0.0 ~ 1.0
    
    # 裁剪顶部比例
    # 0.3 表示裁剪掉图像顶部 30% 的区域
    # 目的: 去除天空、天花板等无关区域，让模型专注于道路
    # 调整建议:
    #   - 室内环境可适当减小 (如 0.2)
    #   - 室外环境可适当增大 (如 0.35)
    CROP_TOP_RATIO = 0.3
    
    # 裁剪底部比例
    # 0.0 表示不裁剪底部
    # 目的: 去除机器人车身、地面近处等区域
    # 调整建议:
    #   - 如果摄像头能看到机器人车身，可设为 0.1 ~ 0.2
    #   - 如果摄像头角度合适，保持 0.0
    CROP_BOTTOM_RATIO = 0.0
